
    ---------- lists by enumeration

    x1 = [1, 9, 42]

    x2 = [3 .. 7]                                       -- [3, 4, 5, 6, 7]

    x3 = [3, 5 .. 10]                                   -- [3, 5, 7, 9]

    x4 = [10, 9 .. 5]                                   -- [10, 9, 8, 7, 6, 5]

    x5 = [100, 90 .. 60]                                -- [100, 90, 80, 70, 60]

    x6 = sum [1,2..10]                                 -- 55
    
    ---------- lists by comprehension

    x7 = [ x + 1 | x <- x1 ]                            -- [2, 10, 43]

    x8 = [ (i,j) | i <- x1, j <- [2, 4]]                -- [(1,2),(1,4),(9,2),(9,4),(42,2),(42,4)]

    x9 = [ n | n <- [1,2,3,4,5,6], even n ]             -- [2, 4, 6]

    x10 = [ (i,j) | i <- x2, even i, j <- x4, odd j]     -- [(4,9),(4,7),(4,5),(6,9),(6,7),(6,5)]

    x11 = [[ x+y | x <- [1,2]] | y <- [9,10]]           -- [[10,11],[11,12]]

    x12 = [ length x | x <- x10 ]                       -- [2,2]

    

    ---------- infinite lists

    x13 = [5, 10 ..]                                    -- [5, 10, 15, 20, ...]

    x14 = repeat 1                                      -- [1, 1, 1, 1, ...]

    x15 = cycle [1,2]                                   -- [1, 2, 1, 2, 1, ...]

    x16 = iterate (\x -> x + 1) 0                       -- [0, 1, 2, 3, 4, ...]
    
    x17 = take 3 [5, 10 ..]                             -- [5, 10, 15, 20, ...]
    
    
    
    
    
    
    
    
    
